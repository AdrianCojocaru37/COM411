#Initilise a empty set

colours = set()
#initiliase not empty
colors ={"blue","red","yellow"}

# Adding elements to the sets

colors.add("purple")
colours.add("red")
colours.add("black")
colours.add("green")

print(colours)
print(colors)

#union --joining two sorted

set1 =colours.union(colors)
print(set1)

#intersection -- finding common elemnts

set2 = colours.intersection(colors)
print(set2)